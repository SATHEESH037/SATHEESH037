
public class Colour {

	public static void main(String[] args) {
	int c=9;
	String colour;
	switch (c) {
	case 1:
		colour="red";
		break;
	case 2:
		colour="pink";
		break;
	case 3:
		colour="white";
		break;
	case 4:
		colour="orange";
		break;
    default:
		colour="no colour in the list";
		break;
	}
	System.out.println(colour);
		
		}

	}
